package day02_Variables;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("\tElif");
        // \t : it gives paragraph space suc as
        // \t :means paragraph space

        System.out.println("\t\t\t\t\t\t Hello");
        //Like that ;)

        System.out.println("CyberTEk School");
        System.out.println("CyberTek \nSchool");
        // \n: means new line, starts with new line

        System.out.println("");//for empty line
        System.out.println("My\nName\nIs\nElif");


        //print: My favorite tv show is "Flash"

        System.out.println("My Favorite Tv show is \"Flash\"");
// \" means double qoute, prints double quote to the console


        System.out.println("\\");
        //prints single slash on console
        System.out.println("/");

        System.out.println("My favorite book is 'Serenad' ");


    }
}
